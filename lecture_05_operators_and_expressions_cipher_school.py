# Operators

# Addition
print(5+5)

#Subtraction
print(10-5)

print(isinstance(10.0,int))

#Division
print(10/2)

# Floor division
print(10//3)

# Modulus
print(10%3)

# Multiplication
print(2*3)

# To the power of
print(2**3)

# Addition of two strings
print("jatin"+"katyal")

# Multiplication of two strings
print("jatin" " " "katyal")

a="abc"
print(a*6)

# Comparison operators

# equal equals to
print(1==2)

# not equals to
print(1!=2)

# less than
print(1<2)
print("ab"<"z")

# greater than
print(1>2)

# Logical operators

# not,and,or
print(True and False)
print(True or False)
print(True and 1)
print(1 and 0)

print(isinstance(True,int))

print(type(True))

print("" and 6)
print(1.6 or "")
